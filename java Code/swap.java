public class swap
{ 
	public static void main(String [] args)
	{
		int a=6;
		int b=9;
		int temp=a;
		a=b;
		b=temp;
		System.out.println("===================");
		System.out.println("       a="+a);
		System.out.println("       b="+b);
		System.out.println("====================");
		
	}
}